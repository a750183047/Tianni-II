void OLED_WrDat(u8 data)
{
	u8 i=8;
	//LCD_CS=0;;
	OLED_DC=1;
  OLED_SCL=0;
  //asm("nop");
  while(i--)
  {
    if(data&0x80){OLED_SDA=1;}
    else{OLED_SDA=0;}
    OLED_SCL=1;
    asm("nop");;
		//asm("nop");
    OLED_SCL=0;;
    data<<=1;
  }
	//LCD_CS=1;
}
void OLED_WrCmd(u8 cmd)
{
	u8 i=8;

	//LCD_CS=0;;
	OLED_DC=0;;
  OLED_SCL=0;;
  //asm("nop");
  while(i--)
  {
    if(cmd&0x80){OLED_SDA=1;}
    else{OLED_SDA=0;;}
    OLED_SCL=1;;
    asm("nop");;
		//asm("nop");
    OLED_SCL=0;;
    cmd<<=1;;
  }

}
void OLED_Set_Pos(u8 x, u8 y)
{
  OLED_WrCmd(0xb0+y);
  OLED_WrCmd(((x&0xf0)>>4)|0x10);
  OLED_WrCmd((x&0x0f)|0x01);
}
void OLED_Fill(u8 bmp_data)
{
	u8 y,x;

	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(bmp_data);
	}
}
void OLED_CLS(void)
{
	u8 y,x;
	for(y=0;y<8;y++)
	{
		OLED_WrCmd(0xb0+y);
		OLED_WrCmd(0x01);
		OLED_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_WrDat(0);
	}
}
void OLED_DLY_ms(u16 ms)
{
  u16 a;
  while(ms)
  {
    a=16000;
    while(a--);
    ms--;
  }
  return;
}

